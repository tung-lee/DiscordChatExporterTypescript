const codeToEmoji: Record<string, string> = {};
for (const [emoji, name] of Object.entries(emojiToCode)) {
  codeToEmoji[name] = emoji;
}

/**
 * Emoji index interface
 */
export const emojiIndex = {
  /**
   * Get emoji name from character
   */
  getCode(emoji: string): string | null {
    return emojiToCode[emoji] ?? null;
  },

  /**
   * Get emoji character from name
   */
  getByName(name: string): string | null {
    return codeToEmoji[name] ?? null;
  },
};
